BEGIN TRANSACTION;
CREATE TABLE score (
    id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    player_name VARCHAR(64) DEFAULT 'Anonymous' NOT NULL,
    score INT(10) DEFAULT 0 NOT NULL,
    sqltime TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);
CREATE TABLE 'keys' ('id' INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,'key' TEXT NOT NULL);
COMMIT;
